/** Display date for blog UI */
export function formatBlogDate(iso) {
	try {
		return new Date(iso).toLocaleDateString('en-IN', {
			year: 'numeric',
			month: 'long',
			day: 'numeric'
		});
	} catch {
		return '';
	}
}
